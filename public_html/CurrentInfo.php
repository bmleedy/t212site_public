<h4>Charter Organization</h4>
<p>BSA Troop 212 is chartered through the parents of Troop 212, a 501(c)3 nonprofit organization operated through the troop committee.</p>
<h4>Troop Meetings</h4>
<p>The Troop meets every Tuesday from 7 p.m. to 8:30 p.m. at the Gig Harbor Elks Lodge. Everyone is welcome at the meetings, and we encourage parents of Scouts to attend the meetings and become involved in planning our activities. Cub Scouts and Webelos are also welcome so they can learn about Boy Scouting. At the meetings we do a lot of Scouting things, including:</p>
<ul>
<li>Discussion of Scouting fundamentals</li>
<li>Learning new Scouting skills</li>
<li>Preparing for upcoming outings and events</li>
<li>Working on merit badge and advancement requirements</li>
<li>Learning about outdoor activities such as camping, hiking, and water sports</li>
</ul>

<h4>Court of Honor</h4>
<p>Every six months one regular troop meeting becomes our quarterly Court of Honor, where the scouts receive awards they have earned over the past months. The Court of Honor is a family event and we ask for the families of scouts in the troop to attend Courts of Honor.</p>

